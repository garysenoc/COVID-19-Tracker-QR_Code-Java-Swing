    void showData(){
              try {
            ResultSet rs = dbh.displayAllRecords();
           
//            
//            while(rs.next()){
//                count++;
//            }
//            
            while(tblDisplayAllRecords.getRowCount()>0){
                ((DefaultTableModel)tblDisplayAllRecords.getModel()).removeRow(0);
            }
            int columns = rs.getMetaData().getColumnCount();
            while(rs.next()){
                Object[] row = new Object[columns];
                for(int i =1;i<=columns;i++){
                    row[i-1] = rs.getObject(i);
                }
                  ((DefaultTableModel)tblDisplayAllRecords.getModel()).insertRow(rs.getRow()-1,row);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(UserForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
