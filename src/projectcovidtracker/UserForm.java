    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int id = Integer.parseInt(txtIDnumber.getText());
        String firstname = txtFirstname.getText();
        String lastname = txtLastname.getText();
        String phone = txtPhonenumber.getText();
        String address = txtAddress.getText();
        
        boolean add = dbh.insertRecord(id,firstname,lastname,phone,address);
        
        if(add){
            JOptionPane.showMessageDialog(this,"User Added");
              String details = txtFirstname.getText() + " " + txtLastname.getText();
        ByteArrayOutputStream out = QRCode.from(details).to(ImageType.JPG).stream();
        File f = new File("C:\\Users\\Gary Senoc\\Pictures\\" + txtLastname.getText() + ".jpg");
        try {
            FileOutputStream fos = new FileOutputStream(f);
            fos.write(out.toByteArray());
            fos.flush();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(UserForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(UserForm.class.getName()).log(Level.SEVERE, null, ex);
        }
            clearText();
            showData();
        }   
        else
            JOptionPane.showMessageDialog(this,"User not added");
    }//GEN-LAST:event_btnAddActionPerformed

